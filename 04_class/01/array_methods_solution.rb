def sort_words(words)
  words.sort
end


def sort_nums(numbers)
  numbers.sort
end


def include_method(teams, team)
  teams.include?(team)
end

def reverse_method(cities)
  cities.reverse
end

def first_method(cities)
  cities.first
end

def last_method(cities)
  cities.last
end

def count_method(cities)
  cities.size
end


def to_array_method(range)
  range.to_a
end
